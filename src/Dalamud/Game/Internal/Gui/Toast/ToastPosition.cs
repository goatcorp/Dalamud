namespace Dalamud.Game.Internal.Gui.Toast
{
    /// <summary>
    /// The positioning of native toast windows.
    /// </summary>
    public enum ToastPosition : byte
    {
        /// <summary>
        /// The toast will be towards the bottom.
        /// </summary>
        Bottom = 0,

        /// <summary>
        /// The toast will be towards the top.
        /// </summary>
        Top = 1,
    }
}
